package com.sygn.xeapp.model;

public class CabListModel {
    private String driverId;
    private String cabName;
    private String cabImage;
    private String cabTime;
    private String cabPrice;


    public String getDriverId() {
        return driverId;
    }

    public void setDriverId(String driverId) {
        this.driverId = driverId;
    }

    public String getCabName() {
        return cabName;
    }

    public void setCabName(String cabName) {
        this.cabName = cabName;
    }

    public String getCabImage() {
        return cabImage;
    }

    public void setCabImage(String cabImage) {
        this.cabImage = cabImage;
    }

    public String getCabTime() {
        return cabTime;
    }

    public void setCabTime(String cabTime) {
        this.cabTime = cabTime;
    }

    public String getCabPrice() {
        return cabPrice;
    }

    public void setCabPrice(String cabPrice) {
        this.cabPrice = cabPrice;
    }
}
